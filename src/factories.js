const hitOrMiss = (spaceStatus) => {
  if (spaceStatus === 0) return "miss";

  if (spaceStatus === 1) return "alreadyHit";

  return "hit";
};

const ship = (length) => ({
  length,
  hitAmount: 0,
  sunk: false,
  hit() {
    this.hitAmount += 1;
  },
  isSunk() {
    if (this.hitAmount === this.length) {
      this.sunk = true;
    }
  }
});

